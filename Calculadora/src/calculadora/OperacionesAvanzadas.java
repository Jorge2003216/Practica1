public class OperacionesAvanzadas {
    public double raizA(double a){
        return Math.sqrt(a);
    }
    public double raizB(double b){
        return Math.sqrt(b);
    }
    public double potencia(double a, double b){
        return Math.pow(a, b);
    }
}
